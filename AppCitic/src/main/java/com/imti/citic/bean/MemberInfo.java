public class MemberInfo implements Serializable{
	private static final long serialVersionUID = 3605730029058606548L;
    /**
     * 客户id
     */
	Integer membId;
	/**
	 * 手机号码
	 */
	String phone;
	/**
	 * 登录密码
	 */
	String membPwd;
	/**
	 * 身份证号
	 */
	String cardNum;
	/**
	 * 验证码
	 */
	String validateCode;
	
	public String getValidateCode() {
		return validateCode;
	}
	public void setValidateCode(String validateCode) {
		this.validateCode = validateCode;
	}
	public Integer getMembId() {
		return membId;
	}
	public void setMembId(Integer membId) {
		this.membId = membId;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getMembPwd() {
		return membPwd;
	}
	public void setMembPwd(String membPwd) {
		this.membPwd = membPwd;
	}
	public String getCardNum() {
		return cardNum;
	}
	public void setCardNum(String cardNum) {
		this.cardNum = cardNum;
	}

	
}
